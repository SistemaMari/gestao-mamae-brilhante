/**
 * Classifica o recém-nascido em PIG, AIG ou GIG conforme curva Intergrowth-21st.
 *
 * @param igSemanas Semanas completas de IG no parto.
 * @param igDias Dias adicionais de IG no parto (0–6).
 * @param pesoG Peso do recém-nascido em gramas.
 * @param sexo 'M' (Masculino) ou 'F' (Feminino).
 * @returns 'PIG' | 'AIG' | 'GIG' ou null se a IG estiver fora da cobertura
 *          da tabela (abaixo de 33+0 ou acima de 42+6).
 */
export function classificarRN(
  igSemanas: number,
  igDias: number,
  pesoG: number,
  sexo: SexoRN,
): ClassificacaoRN | null {
  if (
    !Number.isFinite(igSemanas) ||
    !Number.isFinite(igDias) ||
    !Number.isFinite(pesoG) ||
    pesoG <= 0
  ) {
    return null;
  }

  const key = `${igSemanas}+${igDias}`;
  const tabela = INTERGROWTH_DATA[sexo];
  const entry = tabela?.[key];
  if (!entry) return null;

  if (pesoG < entry.p10) return 'PIG';
  if (pesoG > entry.p90) return 'GIG';
  return 'AIG';
}
